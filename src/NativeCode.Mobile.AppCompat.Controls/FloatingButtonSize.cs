namespace NativeCode.Mobile.AppCompat.Controls
{
    /// <summary>
    /// Enumeration of button sizes.
    /// </summary>
    public enum FloatingButtonSize
    {
        /// <summary>
        /// Indicates to use the default button size.
        /// </summary>
        Default = 0,

        /// <summary>
        /// Indicates to use a small button size.
        /// </summary>
        Mini = 1,

        /// <summary>
        /// Indicates to use a normal button size (default).
        /// </summary>
        Normal = Default
    }
}