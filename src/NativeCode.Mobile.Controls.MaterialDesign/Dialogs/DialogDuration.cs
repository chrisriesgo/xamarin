namespace NativeCode.Mobile.Controls.MaterialDesign.Dialogs
{
    /// <summary>
    /// Enumeration of pre-defined dialog durations.
    /// </summary>
    public enum DialogDuration
    {
        /// <summary>
        /// Indicates that there is no duration.
        /// </summary>
        None = 0,

        /// <summary>
        /// Indicates to show the dialog for a short duration.
        /// </summary>
        Short = 1,

        /// <summary>
        /// Indicates to show the dialog for a longer duration.
        /// </summary>
        Long = 2
    }
}